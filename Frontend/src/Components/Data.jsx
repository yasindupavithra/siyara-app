// Function to get all products including custom ones
export const getAllProducts = () => {
  const customProducts = JSON.parse(localStorage.getItem('customProducts') || '[]');
  return [...products, ...customProducts];
};

// API Response Format for Redux Compatibility
export const getProductsResponse = {
  products: getAllProducts(),
  productsCount: getAllProducts().length,
  resultPerPage: 8,
  filteredProductsCount: getAllProducts().length
};

// Function to get product by ID
export const getProductById = (id) => {
  const allProducts = getAllProducts();
  return allProducts.find(product => product.id === parseInt(id));
};

// Function to filter products
export const filterProducts = (price, brand) => {
  let allProducts = getAllProducts();
  let filtered = [...allProducts];
  
  if (price) {
    filtered = filtered.filter(product => product.price <= price);
  }
  
  if (brand) {
    filtered = filtered.filter(product => 
      product.brand.toLowerCase().includes(brand.toLowerCase()) ||
      product.name.toLowerCase().includes(brand.toLowerCase())
    );
  }
  
  return {
    products: filtered,
    productsCount: filtered.length,
    resultPerPage: 8,
    filteredProductsCount: filtered.length
  };
};